# 创建选项按钮
option_btns = []
var = tk.StringVar()

for i in range(4):
    btn = tk.Radiobutton(root, text="", variable=var, font=("宋体", 12), bg="#f0f0f0", fg="#333333", 
                        activebackground="#e0e0e0", activeforeground="#333333", padx=10, pady=5)
