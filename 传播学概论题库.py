current_index = 0
progress = 0

def show_question():
    global current_index
    if current_index < len(questions):
        q = questions[current_index]
        question_label.config(text=q["question"])
        for i, opt in enumerate(q["options"]):
            option_btns[i].config(text=opt, value=opt[0])
        progress_label.config(text=f"学习进度：{progress}/100")
    else:
        messagebox.showinfo("完成", "恭喜你完成了所有100道题！")
        root.destroy()

def check_answer(selected):
    global current_index, progress
    if not selected:
        messagebox.showwarning("提示", "请选择一个答案！")
        return
    
    q = questions[current_index]
    if selected == q["answer"]:
        messagebox.showinfo("结果", f"答对了！\n解析：{q['analysis']}")
    else:
        messagebox.showinfo("结果", f"答错了！正确答案是{q['answer']}\n解析：{q['analysis']}")
    
    progress += 1
    current_index += 1
    show_question()

